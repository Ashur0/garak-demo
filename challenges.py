CHALLENGE_MAP = {c["id"]: c for c in CHALLENGES}


def run_challenge(challenge_id: int, user_prompt: str) -> dict:
    c = CHALLENGE_MAP.get(challenge_id)
    if not c:
        return {"error": "Challenge not found"}

    from mock_llm import challenge_llm
    response = challenge_llm(user_prompt, c["system_prompt"])
    success = c["validator"](response)

    return {
        "response": response,
        "success": success,
        "flag": c["flag"] if success else None,
        "success_msg": c["success_msg"] if success else None,
    }
